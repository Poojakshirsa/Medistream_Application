package com.example.medistreamapplication.model_form1;

public class DiseaseData {
    private boolean convulsiveDisorders;
    private boolean otitisMedia;
    private boolean dentalCondition;
    private boolean skinCondition;
    private boolean reactiveAirwayDisease;

    // Getters and setters
    public boolean isConvulsiveDisorders() {
        return convulsiveDisorders;
    }

    public void setConvulsiveDisorders(boolean convulsiveDisorders) {
        this.convulsiveDisorders = convulsiveDisorders;
    }

    public boolean isOtitisMedia() {
        return otitisMedia;
    }

    public void setOtitisMedia(boolean otitisMedia) {
        this.otitisMedia = otitisMedia;
    }

    public boolean isDentalCondition() {
        return dentalCondition;
    }

    public void setDentalCondition(boolean dentalCondition) {
        this.dentalCondition = dentalCondition;
    }

    public boolean isSkinCondition() {
        return skinCondition;
    }

    public void setSkinCondition(boolean skinCondition) {
        this.skinCondition = skinCondition;
    }

    public boolean isReactiveAirwayDisease() {
        return reactiveAirwayDisease;
    }

    public void setReactiveAirwayDisease(boolean reactiveAirwayDisease) {
        this.reactiveAirwayDisease = reactiveAirwayDisease;
    }
}
