package com.example.medistreamapplication.model_form2;

public class AdolescentFormModel {
    private boolean difficultyHandlingLifeChanges;
    private boolean sayingNo;
    private boolean feelingTiredOrDepressed;
    private boolean menstrualCycles;
    private boolean regularPeriods;
    private boolean painWhileUrinating;
    private boolean discharge;
    private boolean extremePainMenstruation;


    public boolean isDifficultyHandlingLifeChanges() {
        return difficultyHandlingLifeChanges;
    }

    public void setDifficultyHandlingLifeChanges(boolean difficultyHandlingLifeChanges) {
        this.difficultyHandlingLifeChanges = difficultyHandlingLifeChanges;
    }

    public boolean isSayingNo() {
        return sayingNo;
    }

    public void setSayingNo(boolean sayingNo) {
        this.sayingNo = sayingNo;
    }

    public boolean isFeelingTiredOrDepressed() {
        return feelingTiredOrDepressed;
    }

    public void setFeelingTiredOrDepressed(boolean feelingTiredOrDepressed) {
        this.feelingTiredOrDepressed = feelingTiredOrDepressed;
    }

    public boolean isMenstrualCycles() {
        return menstrualCycles;
    }

    public void setMenstrualCycles(boolean menstrualCycles) {
        this.menstrualCycles = menstrualCycles;
    }

    public boolean isRegularPeriods() {
        return regularPeriods;
    }

    public void setRegularPeriods(boolean regularPeriods) {
        this.regularPeriods = regularPeriods;
    }

    public boolean isPainWhileUrinating() {
        return painWhileUrinating;
    }

    public void setPainWhileUrinating(boolean painWhileUrinating) {
        this.painWhileUrinating = painWhileUrinating;
    }

    public boolean isDischarge() {
        return discharge;
    }

    public void setDischarge(boolean discharge) {
        this.discharge = discharge;
    }

    public boolean isExtremePainMenstruation() {
        return extremePainMenstruation;
    }

    public void setExtremePainMenstruation(boolean extremePainMenstruation) {
        this.extremePainMenstruation = extremePainMenstruation;
    }
}
